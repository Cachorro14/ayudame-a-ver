        $minusBrai = array("⠁","⠃","⠉","⠙","⠑","⠋","⠛","⠓","⠊","⠚","⠅","⠇","⠍","⠝","⠕","⠏","⠟","⠗","⠎","⠞","⠥","⠧","⠺","⠭","⠽","⠵","⠷", "⠮", "⠌", "⠬", "⠾", "⠳", "⠻","⠼⠁","⠼⠃","⠼⠉","⠼⠙","⠼⠑","⠼⠋","⠼⠛","⠼⠓","⠼⠊","⠼⠚","⠄", "⠂", "⠆", "⠒", "⠤","⠢", "⠖", "⠦", "⠣", "⠜", "⠔","⠸⠜", "⠸⠴", "⠶", "⠖", "⠼⠐","⠸", "⠄", "⠠⠂", "⠐⠄", "⠐⠸", "⠸⠂","⠷", "⠾", "⠐", "⠠⠯", "⠢", "⠖","⠷","⠮","⠌","⠬","⠾","⠳","⠻", "⠨⠁","⠨⠃","⠨⠉","⠨⠙","⠨⠑","⠨⠋","⠨⠛","⠨⠓","⠨⠊","⠨⠚","⠨⠅","⠨⠇","⠨⠍","⠨⠝","⠨⠕","⠨⠏","⠨⠟","⠨⠗","⠨⠎","⠨⠞","⠨⠥","⠨⠧","⠨⠺","⠨⠭","⠨⠽","⠨⠵");

